# ‡πÉ‡∏™‡πà r ‡∏Ç‡πâ‡∏≤‡∏á‡∏´‡∏ô‡πâ‡∏≤‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÉ‡∏´‡πâ Python ‡∏≠‡πà‡∏≤‡∏ô \ ‡πÑ‡∏î‡πâ‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á
SOURCE_DIR = r"C:\Users\User\Desktop\wiki.carmensoftware.com"     
DEST_DIR = r"C:\Users\User\Desktop\testchatbot\images"    

EXTENSIONS = {'.png', '.jpg', '.jpeg', '.gif', '.webp'}

def copy_images():
    # 1. ‡πÄ‡∏ä‡πá‡∏Ñ‡∏ß‡πà‡∏≤‡πÇ‡∏ü‡∏•‡πÄ‡∏î‡∏≠‡∏£‡πå‡∏ï‡πâ‡∏ô‡∏ó‡∏≤‡∏á‡∏°‡∏µ‡∏≠‡∏¢‡∏π‡πà‡∏à‡∏£‡∏¥‡∏á‡πÑ‡∏´‡∏°
    abs_source = os.path.abspath(SOURCE_DIR)
    print(f"üßê ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏°‡∏≠‡∏á‡∏´‡∏≤‡πÇ‡∏ü‡∏•‡πÄ‡∏î‡∏≠‡∏£‡πå‡∏ï‡πâ‡∏ô‡∏ó‡∏≤‡∏á‡∏ó‡∏µ‡πà:\n   -> {abs_source}")

    if not os.path.exists(SOURCE_DIR):
        print("\n‚ùå Error: ‡∏´‡∏≤‡πÇ‡∏ü‡∏•‡πÄ‡∏î‡∏≠‡∏£‡πå‡∏ï‡πâ‡∏ô‡∏ó‡∏≤‡∏á‡πÑ‡∏°‡πà‡πÄ‡∏à‡∏≠!")
        print("   ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡πÄ‡∏ä‡πá‡∏Ñ‡∏ß‡πà‡∏≤‡∏ä‡∏∑‡πà‡∏≠‡πÇ‡∏ü‡∏•‡πÄ‡∏î‡∏≠‡∏£‡πå‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á")
        return

    # 2. ‡∏™‡∏£‡πâ‡∏≤‡∏á‡πÇ‡∏ü‡∏•‡πÄ‡∏î‡∏≠‡∏£‡πå‡∏õ‡∏•‡∏≤‡∏¢‡∏ó‡∏≤‡∏á‡∏ñ‡πâ‡∏≤‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà‡∏°‡∏µ
    if not os.path.exists(DEST_DIR):
        os.makedirs(DEST_DIR)
        print(f"‚úÖ ‡∏™‡∏£‡πâ‡∏≤‡∏á‡πÇ‡∏ü‡∏•‡πÄ‡∏î‡∏≠‡∏£‡πå‡∏õ‡∏•‡∏≤‡∏¢‡∏ó‡∏≤‡∏á: {DEST_DIR}")

    print("\nüöÄ ‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤‡πÅ‡∏•‡∏∞‡∏Ñ‡∏±‡∏î‡∏•‡∏≠‡∏Å‡πÑ‡∏ü‡∏•‡πå...")
    count = 0
    found_any_file = False

    for root, dirs, files in os.walk(SOURCE_DIR):
        for file in files:
            found_any_file = True
            ext = os.path.splitext(file)[1].lower()
            
            if ext in EXTENSIONS:
                src_path = os.path.join(root, file)
                dst_path = os.path.join(DEST_DIR, file)

                # ‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£‡∏ä‡∏∑‡πà‡∏≠‡∏ã‡πâ‡∏≥ (Rename ‡∏ñ‡πâ‡∏≤‡∏õ‡∏•‡∏≤‡∏¢‡∏ó‡∏≤‡∏á‡∏°‡∏µ‡πÑ‡∏ü‡∏•‡πå‡∏ä‡∏∑‡πà‡∏≠‡∏ô‡∏µ‡πâ‡∏≠‡∏¢‡∏π‡πà‡πÅ‡∏•‡πâ‡∏ß)
                if os.path.exists(dst_path):
                    base, extension = os.path.splitext(file)
                    i = 1
                    while os.path.exists(os.path.join(DEST_DIR, f"{base}_{i}{extension}")):
                        i += 1
                    dst_path = os.path.join(DEST_DIR, f"{base}_{i}{extension}")

                try:
                    # ‡πÉ‡∏ä‡πâ copy2 ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÄ‡∏Å‡πá‡∏ö metadata ‡πÄ‡∏î‡∏¥‡∏°‡πÑ‡∏ß‡πâ
                    shutil.copy2(src_path, dst_path)
                    print(f"‚úÖ ‡∏Ñ‡∏±‡∏î‡∏•‡∏≠‡∏Å‡πÅ‡∏•‡πâ‡∏ß: {file} -> {os.path.basename(dst_path)}")
                    count += 1
                except Exception as e:
                    print(f"‚ùå ‡∏Ñ‡∏±‡∏î‡∏•‡∏≠‡∏Å‡πÑ‡∏°‡πà‡πÑ‡∏î‡πâ: {file} (Error: {e})")

    if not found_any_file:
        print("\n‚ö†Ô∏è ‡πÑ‡∏°‡πà‡πÄ‡∏à‡∏≠‡πÑ‡∏ü‡∏•‡πå‡∏≠‡∏∞‡πÑ‡∏£‡πÄ‡∏•‡∏¢‡πÉ‡∏ô‡πÇ‡∏ü‡∏•‡πÄ‡∏î‡∏≠‡∏£‡πå‡∏ô‡∏±‡πâ‡∏ô (‡πÇ‡∏ü‡∏•‡πÄ‡∏î‡∏≠‡∏£‡πå‡∏ß‡πà‡∏≤‡∏á‡πÄ‡∏õ‡∏•‡πà‡∏≤?)")
    else:
        print(f"\nüéâ ‡∏™‡∏£‡∏∏‡∏õ: ‡∏Ñ‡∏±‡∏î‡∏•‡∏≠‡∏Å‡πÄ‡∏™‡∏£‡πá‡∏à‡∏™‡∏¥‡πâ‡∏ô‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î {count} ‡∏£‡∏π‡∏õ")
        print(f"üìÇ ‡πÑ‡∏õ‡∏î‡∏π‡∏£‡∏π‡∏õ‡πÑ‡∏î‡πâ‡∏ó‡∏µ‡πà: {DEST_DIR}")

if __name__ == "__main__":
    copy_images()